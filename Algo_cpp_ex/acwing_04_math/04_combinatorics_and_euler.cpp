#include <iostream>
#include <vector>

using namespace std;

typedef long long LL;

const int mod = 1e9 + 7;

// ==========================================
// 1. 求单个数的欧拉函数 Euler's Totient Function \phi(N)
// 定义：1~N 中与 N 互质的数的个数。 比如 1~12 中，和 12 互质的是 {1, 5, 7, 11}，共 4 个。
//
// 【数学原理推导：从“容斥原理”到“纯乘法公式”的降维打击】
// 想象我们要把 1~12 中所有包含公因子的数给“筛掉”寻找互质独苗：
// 因为 12 的质因子“纯正基因”只有 2 和 3。
//   1. 先在这个集合中杀掉所有 2 的倍数（存活下来了 12 * (1 - 1/2) 个）
//   2. 在活下来的兄弟中，再杀掉所有 3 的倍数（再乘上它的存活率 (1 - 1/3) ）
// 综上，只要遇到一个有着根本影响力的质因子基底 p_i，存活对象就会被砍掉批量 (1/p_i)，乘上存活率 (1 - 1/p_i)。
// 由此我们得出了一个极其优美且不靠暴力遍历的无敌通式：
// 公式：\phi(N) = N * (1 - 1/p1) * (1 - 1/p2) * ... * (1 - 1/pk)
// （注意：公式里计算的是每种质数，如果某个质数比如 2 出现了多次，没关系，只算一次它的种类“削减率”。）
//
// 结论扩展：欧拉函数是积性函数；若 a, n 互质，老面孔欧拉定理告知我们 a^{\phi(n)} ≡ 1 (mod n)。
int phi(int n) {
    int res = n; // res 起手捏着满血的总基数 N

    // 实操：完全套用了我们前置知识“试除法分解质因数”的外壳
    // 用最少的 O(sqrt(N)) 动作，揪出它所有的质因子底座
    for (int i = 2; i <= n / i; i++) {
        if (n % i == 0) { // 逮到一个能整除的质因数 i，准备发动技能！
            
            // 理论上我们应该写：res = res * (1 - 1/i)
            // 两派代数等价展开：res = res - res / i， 提出公因式后变成：res = (res / i) * (i - 1)
            // 【为什么要用后面这种极其反人类的写法？】
            // 因为 C++ 必须保佑它是整数运算！先 /i 可以确保前面除尽绝不留浮点死角，再同乘 (i-1) 是为了防止传统 res*(i-1) 引发的中间态 int 原地爆炸溢出。
            res = res / i * (i - 1); 
            
            // 纯粹地扫除赘余机制：既然质因子 i 这类物种已经贡献过它的 (1 - 1/i) 除草杀伤率了，
            // 它的几次方翻倍也都没什么卵用（只计算该组种类），所以循环把它彻底除干净榨干！
            while (n % i == 0) n /= i; 
        }
    }
    
    // 扫尾防漏：如果最后 n 被除以了这么多依然大于 1，说明存在一个大于 sqrt(N) 的超级刺头原始质因子。
    // 这也是孤本种类，直接补它一刀。
    if (n > 1) res = res / n * (n - 1);
    
    return res;
}

// ----------------------------------------------------
// 【进阶用法：线性筛法批量求 1~N 所有数的欧拉函数数列】
// 需求场景：查 1 次用上面直接算。但如果老板让你把 1 到 100 万所有的欧拉函数全部拍在桌上，刚才的 O(N * \sqrt{N}) 会瞬间死机。
// 解决之道：我们把这份高强度的排查工作，借用前面的小弟“欧拉筛（线性筛质数）”过程，
// 在它 O(N) 像割韭菜一样找质数时，我们搭个顺风车，把每个数字的欧拉函数给它附魔附出来！
//
const int N = 100010;
int primes[N], cnt;
bool st[N];
int euler[N]; // 存货仓库：euler[i] 表示 \phi(i)

void get_eulers(int n) {
    euler[1] = 1; // 1 与自己唯一的互质尊严，打好地基
    
    for (int i = 2; i <= n; i++) {
        if (!st[i]) {
            primes[cnt++] = i;
            // 【定理 1：纯血觉醒】
            // 如果 i 能坚持到这都没留下抓痕 `!st[i]`，说明它是天选的“质数”。
            // 既然是质数，那从小到它脚下所有（除了它自己）的统统跟它互质！
            euler[i] = i - 1; 
        }
        
        // 接下来步入经典的线性筛内层循环：通过每一个 p_j 去戳后面的大合数
        for (int j = 0; primes[j] <= n / i; j++) {
            st[primes[j] * i] = true;
            
            // 核心分水岭：检查当前要合成的大数字 `p_j * i`，它的血统成分是什么
            if (i % primes[j] == 0) {
                // 【定理 2：老朋友基因共振】
                // primes[j] 已经是 i 的体内能找到的某个成型底因数了。（基因种类未翻新）
                // 我们重新看刚背的公式通解： \phi = N * (1-1/p1)*(1-1/p2) ...
                // 和原来 \phi(i) 唯一唯一的区别就是：排头的总基数 N 强行膨胀了 primes[j] 倍；
                // 但是后面那一屁股去重的削减率护甲 (1-1/pk) 这个库里边，【一个新种类的 p 都没有加进来】！
                // 结论极其霸道粗暴：什么都不需要额外乘，跟着总基数纯数字放大即可！
                euler[primes[j] * i] = euler[i] * primes[j];
                break; // 欧拉筛特质：为了保住 O(N) 必须在此斩断，用最小素因数进行过滤！
            }
            
            // 【定理 3：异卵新基因碰撞】
            // 如果坚挺地经过了上面那关（没有发生 break），这说明 i 内部绝对找不到当前这枚小素数 primes[j]。
            // 所以，这也是两个毫不相干、【互相互质】的两波物种合并！
            // 这里用上了著名规律“积性函数护法”： 当 A 分子和 B 分子彻底纯互质时，\phi(A * B) = \phi(A) * \phi(B)
            // 所以 \phi(primes[j] * i) = \phi(i) * \phi(primes[j])
            // 又因为 primes[j] 本身是个小质数，它的 \phi 结果刚刚讲了，等于它本身减去个 1 啦！
            // 把 \phi(primes[j]) 替换成 (primes[j] - 1)，结果出炉：
            euler[primes[j] * i] = euler[i] * (primes[j] - 1);
        }
    }
}

// ==========================================
// 2. 组合数计算 (Combinatorics Calculation)
// 求 $C_a^b \pmod p$ 的三种常用计算方式

// 类型 I： $a,b \le 2000$ (数据范围较小)，适用于频繁的预处理查询 O(N^2)
int c[2010][2010]; 
void init_C() {
    for (int i = 0; i <= 2000; i++) {
        for (int j = 0; j <= i; j++) {
            if (!j) c[i][j] = 1;
            else c[i][j] = (c[i - 1][j] + c[i - 1][j - 1]) % mod;
        }
    }
}

// 类型 II： $a,b \le 10^5$，可以预处理阶乘及其逆元 O(N log p)，查询 O(1)
int fact[N], infact[N]; // 阶乘与阶乘逆元

LL qmi_inv(LL a, LL k, LL p) {
    LL res = 1;
    while (k) {
        if (k & 1) res = res * a % p;
        a = a * a % p;
        k >>= 1;
    }
    return res;
}

void init_fact() {
    fact[0] = infact[0] = 1;
    for (int i = 1; i < N; i++) {
        fact[i] = (LL)fact[i - 1] * i % mod;
        infact[i] = (LL)infact[i - 1] * qmi_inv(i, mod - 2, mod) % mod;
    }
}

// O(1) 求 C(a, b) = a! / (b! * (a-b)!) % mod
int C(int a, int b) {
    if (a < b || a < 0 || b < 0) return 0;
    return (LL)fact[a] * infact[b] % mod * infact[a - b] % mod;
}

// 卢卡斯定理 (Lucas Theorem)：用于 $a,b \le 10^{18}$ 且质数 $p \le 10^5$
// 在密码系统或极大的离散对数计算经常出现。

int main() {
    cout << "phi(12) = 1 ~ 12 之间互质的个数 = " << phi(12) << endl; 
    // 互质的是 {1, 5, 7, 11} -> 4.

    get_eulers(20);
    cout << "线性筛结果 euler[12] = " << euler[12] << endl;

    init_fact();
    cout << "组合数计算: C(5, 2)  = " << C(5, 2) << endl;

    return 0;
}
