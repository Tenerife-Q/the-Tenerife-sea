// 【求逆元方法 2】：费马小定理 (Fermat's Little Theorem)
// 适用条件：p 必须是质数，且 a 不是 p 的倍数。在算法竞赛中常用于质数模数(如 1e9+7)。
// 定理内容：a^(p-1) ≡ 1 (mod p)
// 推导逆元：同乘 a^(-1)  =>  a^(p-2) ≡ a^(-1) (mod p)
// 即：a 的乘法逆元是 a^(p-2)。
// 
// 费马小定理证明简述：
// 1. 考虑集合 P = {1, 2, ..., p-1}，长度为 p-1。
// 2. 用 a 去乘集合里的每一个数，得到 A = {a, 2a, ..., (p-1)a}。
// 3. 因为 a 与 p 互质，A 中没有任何一个数能被 p 整除，并且 A 中的数模 p 后两两不同。
// 4. 因此，集合 A 模 p 后的结果，其实就是集合 P 的一个排列（只是顺序打乱了）。
// 5. 让两个集合内部所有元素各自相乘：
//    1 * 2 * ... * (p-1) ≡ a * 2a * ... * (p-1)a (mod p)
//    (p-1)! ≡ a^(p-1) * (p-1)! (mod p)
// 6. 因为 p 是质数，(p-1)! 不含因子 p，所以可以约掉，得到 1 ≡ a^(p-1) (mod p)。

void get_inv_fermat(LL a, LL p) {
    if (a % p == 0) puts("impossible"); // 不互质，且 p 为质数，此时无逆元
    else printf("%lld\n", qmi(a, p - 2, p));
}

// ----------------------------------------------------
// 【求逆元方法 3】：欧拉定理 (Euler's Theorem) - (费马小定理的普适版)
// 适用条件：p 可以不是质数，只要 gcd(a, p) = 1。
// 定理内容：a^φ(p) ≡ 1 (mod p)  【φ(p) 是欧拉函数：代表 1~p 中与 p 互质的数的个数】
// 推导逆元：a * a^(φ(p)-1) ≡ 1 (mod p)  =>  逆元就是 a^(φ(p)-1)
// 
// 欧拉定理证明简述：
// 证明逻辑与费马小定理完全一致！只不过原来的集合 P ={1,2..p-1} 被替换成了
// "与 p 互质的 φ(p) 个数组成的缩系"。两边连乘，约底即可。
// 注：当 p 是质数时，1~p-1 全和它互质，因此 φ(p) = p-1。欧拉定理就自然退化成了费马小定理。
// 通常要求逆元时，如果不用扩欧，就要先花 O(sqrt(n)) 算一遍欧拉函数，不如扩欧直接。


// 扩欧求一元线性同余方程 $ax \equiv b \pmod m$
// 它等价于存在整数 y 使得 $ax + my = b$。
// 根据裴蜀定理，方程有解当且仅当 gcd(a, m) | b。
// 我们先通过 `exgcd(a, m, x, y)` 求出 $ax0 + my0 = gcd(a, m)$
// 再将等式同乘 $b / gcd$ 即可得到 x 的特解 $x = x0 * (b/gcd)$。
bool linear_congruence(int a, int b, int m, int &x) {
    int _x, _y;
    int d = exgcd(a, m, _x, _y);
    if (b % d) return false; // 无解
    
    // x = _x * (b / d)
    // 防止 C++ 负数取模向 0 取整带来错误：(res % m + m) % m。
    x = (LL)_x * (b / d) % m; // 将解转移到 [0, m) 区间
    return true;
}

int main() {
    // 快速幂测算: 3^5 mod 13 = 243 % 13 = 9
    cout << "3^5 mod 13 = " << qmi(3, 5, 13) << endl;

    // 费马求逆元：求 3 在模 13 下的逆元 (结果应是 9。因为 3 * 9 = 27 = 2 * 13 + 1 -> 27=1 mod 13)
    cout << "3 mod 13 的乘法逆元: " << qmi(3, 13 - 2, 13) << endl;

    // 扩展欧几里得测算: a = 12, b = 8 => gcd = 4
    int x, y;
    int g = exgcd(12, 8, x, y);
    cout << "exgcd(12, 8): gcd = " << g << ", 12*(" << x << ") + 8*(" << y << ") = " << g << endl;

    // 线性同余测试: a=2, b=4, m=6  即 2 * x = 4 (mod 6) => x = 2
    int res_x;
    if (linear_congruence(2, 4, 6, res_x)) {
        cout << "2 * " << res_x << " = 4 (mod 6) [可能有多个解, 这里给出一个特解]" << endl;
    }

    return 0;
}
