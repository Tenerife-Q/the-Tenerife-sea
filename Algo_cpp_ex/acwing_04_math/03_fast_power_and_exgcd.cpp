//
int exgcd_iterative(int a, int b, int &x, int &y) {
    int x0 = 1, y0 = 0; // 对应于 a 的系数
    int x1 = 0, y1 = 1; // 对应于 b 的系数

    while (b != 0) {
        int q = a / b;  // 计算商
        
        // 1. 同步更新余数 (对应普通的 a, b 滚动)
        int r = a % b;  
        a = b; 
        b = r;

        // 2. 同步更新 x 的系数矩阵变换
        int x2 = x0 - q * x1; 
        x0 = x1; 
        x1 = x2;

        // 3. 同步更新 y 的系数矩阵变换
        int y2 = y0 - q * y1; 
        y0 = y1; 
        y1 = y2;
    }
    
    // b 归 0 时，a 就是 gcd，这时的 (x0, y0) 就是答案
    x = x0;
    y = y0;
    return a; 
}




