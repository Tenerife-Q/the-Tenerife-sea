// ==========================================
// 【补充引申】：非递归迭代版扩展欧几里得 (Iterative Extended GCD)
// 参考资料：初等代换矩阵 / 列表迭代法
// 虽然递归版代码只有 5 行极其优雅，但在极其极端的情况下（或者防爆栈、追求常数级优化时），
// 我们可以用非递归（迭代）的方式来实现 exgcd。
//
// 迭代版的本质是维护一个 2x2 的状态矩阵，或者说记录当前步骤的前两层系数。
// 设初始我们有两组等式：
// ① a * 1 + b * 0 = a   => (x_0=1, y_0=0) 产生 a
// ② a * 0 + b * 1 = b   => (x_1=0, y_1=1) 产生 b
//
// 辗转相除的规律是，新的余数 r = a - q * b (其中 q = a / b)。
// 我们只需对应地将它的系数也做相同的线性变换即可：
// 新的 x = x_0 - q * x_1
// 新的 y = y_0 - q * y_1
// 然后不断滚动覆盖 (x0, y0) 和 (x1, y1)，直到 b = 0 停止。
//
// ----------------------------------------------------
// 【实质案例运行模拟】：以 a = 240, b = 46 为例演示扩展欧几里得算法
//
// 算法结束条件与欧几里得算法一致，即 r_{i+1} = 0，此时所得的 s_i 和 t_i 
// 即满足等式 gcd(a, b) = r_i = a * s_i + b * t_i。
//
// 1. 表格推导：所得的最大公因数是2，得到裴蜀等式为 gcd(240, 46) = 2 = -9*240 + 47*46
// | 序号 i | 商 q_{i-1}   | 余数 r_i           | s_i             | t_i               |
// |--------|--------------|--------------------|-----------------|-------------------|
// | 0      |              | 240                | 1               | 0                 |
// | 1      |              | 46                 | 0               | 1                 |
// | 2      | 240 / 46 = 5 | 240 - 5 * 46 = 10  | 1 - 5 * 0 = 1   | 0 - 5 * 1 = -5    |
// | 3      | 46 / 10 = 4  | 46 - 4 * 10 = 6    | 0 - 4 * 1 = -4  | 1 - 4 * -5 = 21   |
// | 4      | 10 / 6 = 1   | 10 - 1 * 6 = 4     | 1 - 1 * -4 = 5  | -5 - 1 * 21 = -26 |
// | 5      | 6 / 4 = 1    | 6 - 1 * 4 = 2      | -4 - 1 * 5 = -9 | 21 - 1 * -26 = 47 | -> (求得 gcd)
// | 6      | 4 / 2 = 2    | 4 - 2 * 2 = 0      | 5 - 2 *-9 = 23  | -26 - 2 * 47 = -120|
// 
// 2. 初等变换矩阵推导：
// 这个过程也可以用矩阵的初等列变换表示。第一行表示当前的参数组，二三行分别为对应系数：
// 
// / 240  46 \    / 10  46 \    / 10   6 \    /  4   6 \    /  4   2 \
// |   1   0 | -> |  1   0 | -> |  1  -4 | -> |  5  -4 | -> |  5  -9 |
// \   0   1 /    \ -5   1 /    \ -5  21 /    \-26  21 /    \-26  47 /
//
// - 当出现右侧最后一列对应第一栏的值为 `gcd=2` 时，列中的系数组合正是 -9 与 47。
// - 得到 -9 * 240 + 47 * 46 = 2
// - 此时代码中不断更迭覆盖的 (x0, y0) 和 (x1, y1) 正是矩阵中交替向前的这两列。
