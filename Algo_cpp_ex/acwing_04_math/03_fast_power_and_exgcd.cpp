// ==========================================
// 2. 乘法逆元与相关的数论定理 (Multiplicative Inverse & Number Theory)
// 
// 核心问题：在模 p 意义下，如何计算 a 的倒数 (即乘法逆元 x)，使得 a * x ≡ 1 (mod p)？
//
// ----------------------------------------------------
// 【第一步：欧几里得算法与逆元存在的基础 —— 裴蜀定理 (Bézout's Lemma)】
// 
// 在理解裴蜀定理与扩展欧几里得之前，我们必须先理清最基础的普通欧几里得算法：
// 
// [前置：普通欧几里得算法 (辗转相除法)]
// 作用：求两个数 a 和 b 的最大公约数 gcd(a, b)。
// 核心推导公式：gcd(a, b) = gcd(b, a mod b) 
// 数学原理证明：
// 1. 将 a 除以 b 得到商 q 和余数 r，即写成带余除法：a = q * b + r。
//    其中 q = ⌊a/b⌋，余数 r = a mod b = a - q * b。
// 2. 假设整数 d 是 a 和 b 的任意公约数（即 d 能整除 a 且 d 能整除 b，记作 d|a, d|b）。
// 3. 既然 d 整除 a 且整除 b，由 r = a - q * b 可知，d 必然也能整除 r。
//    这说明：a 和 b 的公约数，一定也是 b 和 r (即 a mod b) 的公约数。
// 4. 反过来同理：如果 d' 是 b 和 r 的公约数 (d'|b 且 d'|r)，
//    根据 a = q * b + r，d' 也必然能整除 a。
// 5. 结论：两对组合 (a, b) 和 (b, a mod b) 拥有【完全相同】的公约数集合！
//    既然公约数集合完全相同，那么它们其中的"最大公约数"必然也完全相等。
//    即：gcd(a, b) = gcd(b, a mod b)。
//
// 6. 为什么不断往下递归，直到余数为 0，就能找到最大公约数？
//    - 每次递归，我们都把问题规模变小：(a, b) -> (b, r1) -> (r1, r2) -> ... -> (r_n, 0)。
//    - 因为余数总是不断减小的 (b > r1 > r2 ... ≥ 0)，所以最终余数一定会变成 0。
//    - 当到达状态 gcd(x, 0) 时，这意味着什么？
//      根据步骤 5，gcd(a, b) 的值等于 gcd(x, 0) 的值。
//      任何非零整数 x 和 0 的公约数有哪些？因为 0 能被任何非零整数整除 (0 = x * 0)，
//      所以 x 和 0 的公约数集合，其实就是 x 自己的所有约数。
//      那 x 的所有约数中，最大的自然就是 x 本身！
//    - 所以 gcd(x, 0) = x。这就是辗转相除法的终点与精髓。
//
// ===> 过继到裴蜀定理 ===>
//
// 普通欧几里得告诉我们“怎么求”最大公约数，而裴蜀定理揭示了 gcd(a, b) 最深刻的“代数本质”：
// 
// [裴蜀定理 / 贝祖等式 (Bézout's Lemma)]
// 参考：维基百科 / OI-Wiki
// 完整官方表达：
// 设 a, b 是不全为零的整数，则存在整数 x, y，使得 a*x + b*y = gcd(a, b) 成立。
// 并且，对于任意的整数 x, y，线性组合 a*x + b*y 一定是 gcd(a, b) 的倍数。
//
// 换句话说，集合 S = {ax + by | x, y ∈ Z, ax + by > 0} 
// (也就是 a 和 b 所能组合出的所有正整数) 中的【最小正整数】，恰好就是 gcd(a, b)！
//

// ----------------------------------------------------
// 严格证明过程 (利用良序定理原理)：
// [铺垫]
// 1. 构造一个集合 S = {a*u + b*v | u, v ∈ Z 且 a*u + b*v > 0} 
//    （也就是 a 和 b 所有的正整数线性组合）。
// 2. 显然 S 是非空的（比如取 u=a, v=b，结果是 a^2+b^2 > 0）。
// 3. 根据自然数的“良序定理”（任何非空正整数集合中必然存在最小的元素），设 S 中的最小正整数为 d。
//    因为 d 在 S 中，所以存在整数 x_0, y_0，使得 d = a * x_0 + b * y_0。
//
// [核心引理 1：证明 d 必能整除 a 和 b（即 d 是公约数）]
// 1. 我们利用带余除法，将 a 除以 d，得到商 q 和余数 r，即：a = q*d + r （其中 0 ≤ r < d）。
// 2. 把 d = a*x_0 + b*y_0 代入，移项求 r：
//    r = a - q*d
//      = a - q(a*x_0 + b*y_0)
//      = a(1 - q*x_0) + b(-q*y_0)
// 3. 结果显示，余数 r 也能表示为 a 和 b 的线性组合！
// 4. 重大发现：如果 r > 0，那 r 也是集合 S 的元素。但是我们的前提是 r < d，而 d 是 S 中“最小”的正整数，这就产生矛盾了！
// 5. 唯一解决矛盾的方法就是 r = 0。即 a 被 d 整除 (d | a)。同理可证 d | b。
// 6. 到此，我们证明了 d 是 a 和 b 的公约数。
//
// [核心引理 2：证明 d 是最大公约数 (gcd)]
// 1. 假设 c 是 a 和 b 的任意其他公约数（即 c|a 且 c|b）。
// 2. 那么 c 必然能整除 a 和 b 的任意线性组合。
// 3. 因为 d = a*x_0 + b*y_0，所以 c 也必然能整除 d (c | d)。
// 4. 一个数能整除另一个正整数，说明 c ≤ d。
// 5. 既然 d 既是 a,b 的公约数，又比所有其他公约数都大，那 d 毫无疑问就是最大公约数 gcd(a, b)。
//
// 证明完毕：gcd(a, b) = a*x_0 + b*y_0。
//
// ----------------------------------------------------
// 【引申：这与乘法逆元有什么关系？（逆元存在的充要条件）】
// 我们要求 a 在模 p 意义下的逆元 x，也就是解这个同余方程：
//     a * x ≡ 1 (mod p)
// 这个同余式的等价数学含义是：a*x 除以 p 的余数是 1。
// 写成等式就是：a*x = p * (-y) + 1  (这里的 -y 就是商，移项其实就是 a*x + p*y = 1)
//
// 现在把这个方程 a*x + p*y = 1 放到刚证完的裴蜀定理里看：
// 根据定理，ax + py 能凑出的“最小正整数是 gcd(a, p)”，且能凑出的所有数都是 gcd(a, p) 的倍数。
// 要想让 ax + py 恰好等于 1（方程有解），唯一的可能就是：
//     gcd(a, p) 必须等于 1！
// 
// 结论：a 存在模 p 乘法逆元的【充要条件】就是 gcd(a, p) = 1 (即 a 与模数 p 互质)。
// ----------------------------------------------------
// 
// 【求逆元方法 1】：扩展欧几里得算法与普通欧几里得的“过继”
// (Extended Euclidean Algorithm, exgcd)
// 
// 适用条件：p 不一定是质数，只要 gcd(a, p) = 1 即可。最普适的方法！
// 原理：直接用 exgcd 求方程 a*x + p*y = gcd(a, p) = 1 的解 (x, y)，x 就是逆元。
//
// 核心思想：既然普通欧几里得算法在不断递归：(a, b) -> (b, a%b) -> ... -> (d, 0)
// 且裴蜀定理告诉我们必然有 ax + by = d。
// 那么我们能不能在普通欧几里得“**回溯**”的时候，把 (x, y) 顺便推导出来？
// 
// 扩展欧几里得递推公式数学推导 (从普通 gcd 过继)：
// 1. 已知普通欧几里得算法：gcd(a, b) = gcd(b, a % b)。
// 2. 假设在下一层递归中，我们已经求出了 gcd(b, a % b) 并且得到了它的系数解 (x', y')：
//    也就是说，此时我们有：b * x' + (a % b) * y' = gcd
// 3. 我们要把 (a % b) 在数学上展开：a % b = a - ⌊a / b⌋ * b。 (在 C++ 中 ⌊a/b⌋ 就是 a/b)
// 4. 将其次子代入上一层的等式中：
//    b * x' + (a - (a / b) * b) * y' = gcd
// 5. 将上式按 a 和 b 的项重新排列，展开括号：
//    b * x' + a * y' - b * (a / b) * y' = gcd
// 6. 提取 a 和 b 的公约数，合并同类项寻找对应关系：
//    a * (y') + b * (x' - (a / b) * y') = gcd
// 
// 这不就恰好对应着我们这一层需要求解的目标方程：a * x + b * y = gcd 吗？！
// 
// 系数严丝合缝对比：
// 第一项 a 的系数：当前层的 x = 下一层的 y'
// 第二项 b 的系数：当前层的 y = 下一层的 (x' - (a / b) * y')
//
// 巧记：
// 新的 x 等于 旧的 y，
// 新的 y 等于 旧的 x 减去 (a对b下取整)*旧的y。
// 
// 这样在 gcd(x, 0) 摸到底的时候，显然 x = 1, y = 0 即可：因为 x*1 + 0*0 = x。
// 然后逐步回溯，就能求出最开始最根本的解 (x, y)！
//
// ----------------------------------------------------
// 【实质案例运行栈模拟】：以主函数中的 exgcd(12, 8, x, y) 为例
// 目标求解方程：12*x + 8*y = gcd(12, 8)
//
// [向下探底递归过程]
// 层_1：a=12, b=8。执行出 d = exgcd(8, 4, y, x)
// 层_2：a=8 , b=4。执行出 d = exgcd(4, 0, y, x)
// 层_3：a=4 , b=0。触发 if(!b) 边界条件！
//
// [向上回溯结算过程]
// 必须理解一个核心：由于调用时交叉了参数 `exgcd(b, a % b, y, x);`
// 这意味着：【下层求出的 x】会直接存进【本层的 y】；【下层求出的 y】会直接存进【本层的 x】！
// 这使得数学推导中那个"兵不血刃"的等式`本层x = 下层y`瞬间自动完成。
// 最后只需修整执行这一句 `y -= a / b * x;` 。
//
// 具体代码数值回溯如下：
// ① 层_3 触底结算 (a=4, b=0)：
//    匹配边界，定锚初值 x_3 = 1, y_3 = 0，返回 d = 4。
// 
// ② 退至层_2 结算 (a=8, b=4)：
//    因交叉接收，此刻它手里的变量被装进：x_2 = (y_3) = 0，y_2 = (x_3) = 1。
//    接着执行它自己的修整代码：y_2 = y_2 - (8/4) * x_2
//                            y_2 = 1 - 2 * 0 = 1。
//    层2结束时：x_2 = 0, y_2 = 1。继续向上弹栈。
//
// ③ 退至顶层_1 结算 (a=12, b=8)：
//    再次交叉接收：本顶层手里的变量被装进：x_1 = (y_2) = 1，y_1 = (x_2) = 0。
//    接着执行修整代码：y_1 = y_1 - (12/8) * x_1 （注：12/8 整除等于 1）
//                    y_1 = 0 - 1 * 1 = -1。
//    顶层执行完毕，全局答案诞生：x = 1, y = -1。
//
// 输出方程验证：12*(1) + 8*(-1) = 4 = gcd。完美闭环！
// ----------------------------------------------------
//
int exgcd(int a, int b, int &x, int &y) {
    if (!b) {
        x = 1, y = 0; // ax + 0 = a -> x=1
        return a;
    }
    // 递归拿到下一层的 x, y
    // 注意这里我们非常巧妙地把参数 y 传给了形参 x_next, x 传给了 y_next
    // 这样在下层返回后，当前的 x 已经变成了子层的 y1，也就是 x = y1 这一步自动完成了。
    int d = exgcd(b, a % b, y, x); 
    // 现在 x 里装的是 y1, y 里装的是 x1。
    // 我们只需要更新 y = x1 - (a/b)*y1，即：
    y -= a / b * x;
    return d;
}

