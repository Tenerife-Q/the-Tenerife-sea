// 3. 博弈论 Nim 游戏 (Game Theory)
// n 堆石子，每次取任意多个，可以取一堆。
// 结论：当 $a_1 \oplus a_2 \oplus \dots \oplus a_n \neq 0$ 时，先手必胜。
bool nim_game(const vector<int>& piles) {
    int res = 0;
    for (int p : piles) {
        res ^= p;
    }
    return res != 0;
}

int main() {
    // Nim 游戏，如果石子堆有两个，分别是 2, 3，异或和不为0，先手必胜。
    cout << "Nim Game (2, 3) 结果: " << (nim_game({2, 3}) ? "先手必胜" : "后手必胜") << endl;
    return 0;
}
