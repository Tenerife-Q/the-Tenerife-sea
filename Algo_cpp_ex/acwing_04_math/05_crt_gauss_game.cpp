// 2. 高斯消元法 (Gaussian Elimination) O(N^3)
// ==========================================
/*
 * [算法物理模型：网格流水线消消乐]
 * 我们要解 N 个未知数的方程组，把整个组提取成一个 N * (N+1) 的浮点数矩阵。
 * 核心控制指针有两个：
 * c (Column / 列指针)：代表当前正在处理第几个“未知数”（列索引，从左到右 0 到 n-1）。
 * r (Row / 行指针)：代表当前成功固定了几条“有效方程”（行索引，从上到下 0 到 n-1）。
 * 
 * [矩阵变换流程分析]
 * 1. 找主元 (Pivot)：在第 c 列中，从第 r 行向底部遍历，找到绝对值最大元素的行号 t。
 *    (使用最大值的原因：在后续除法操作中，除数越大，浮点数的精度误差扩散越小)。
 *    - 如果这一列最大元素都 < eps，说明整个子列都是 0。此未知数对后续方程无影响/无解限制，c 继续前移，但 r 停留在原地（此时就会导致最终 r < n）。
 * 
 * 2. 交换行 (Swap)：将拥有主要元素的第 t 行，与当前正在处理的第 r 行整行对换。
 *    (边界细节：交换从第 c 列开始即可，因为前 c-1 列早已在之前的循环中被化为了 0)。
 * 
 * 3. 归一首元 (化1)：将第 r 行的所有数字除以本行首首个非零元素 (原本的 a[r][c])，使其变为 1。
 *    (核心细节 - 倒序遍历：for(i = n; i >= c; i--)。如果正序遍历，a[r][c] 自身会先被除成 1，
 *    导致后面 j 索引提取 a[r][c] 作除数时，提取到的是被污染后的 1，而非原始系数。)
 * 
 * 4. 整体下消 (消0)：用已变为 1 的 a[r][c]，去消灭第 r 行下方所有行 (i > r)在第 c 列的元素。
 *    (核心细节 - 倒序遍历：同样必须倒序 j = n -> c，计算 a[i][j] -= a[r][j] * a[i][c]。
 *    如果正序，提取的 a[i][c] 会提前被自己减成 0，后续的倍数乘积将全部失效。)
 * 
 * 5. r++：此列处理成功，有效方程计数器 r 推进 1。
 * 
 * [结果判定与回代 (Back Substitution)]
 * A. 检查无效行 (r < n)：
 *    当循环结束，如果有效方程数(r) < 未知数(n)，说明矩阵底部出现了全零行。
 *    验证左侧系数为 0 的同时，右侧常数项 a[i][n] 是否也为 0：
 *    - 若 a[i][n] != 0：产生了 0 == 非零常数 的悖论，系统无解 (return 2)。
 *    - 若 a[i][n] == 0：恒等式成立，存在自由变量，系统有无穷多解 (return 1)。
 * 
 * B. 回代过程 (求解唯一解)：
 *    若打成完美的上三角矩阵(如行长为 0 0 1 | 5 的形式)，则最后一行直接给出 x_{n-1} = a[n-1][n]。
 *    回代逻辑从倒数第二行开始向上推演。
 *    例如推导倒数第二行 (拥有 x_{n-2} 和 x_{n-1})：
 *    等式：x_{n-2} + a[n-2][n-1] * x_{n-1} = a[n-2][n]
 *    由于底层的 x_{n-1} 已在 a[n-1][n] 处存好，直接移项：
 *    a[n-2][n] -= a[n-2][n-1] * a[n-1][n]  -> 这就是 a[i][n] -= a[i][j] * a[j][n] 的由来。
 * 
 * [可视化：矩阵变换全过程演示 (结合代码步步映射)]
 * 假设我们要解 N=3 的方程组：
 *  -1x0 + 2x1 + 1x2 = 2
 *   3x0 + 8x1 + 1x2 = 24
 *   2x0 - 1x1 + 1x2 = 7
 * 
 * 初始增广矩阵：
 *  [ -1   2   1 |  2 ] -> 行 0
 *  [  3   8   1 | 24 ] -> 行 1  (此时第0列 |3| 最大)
 *  [  2  -1   1 |  7 ] -> 行 2
 *
 * -----------------------------------------
 * 循环 第 0 列 (c=0, r=0)：
 * 【代码：寻主元并对调】 for (int i = c; i <= n; i++) swap(a[t][i], a[r][i]);
 * 动作：因为第0列绝对值最大是3，行0与行1对调：
 *  [  3   8   1 | 24 ] -> 新防线，行 0
 *  [ -1   2   1 |  2 ] 
 *  [  2  -1   1 |  7 ] 
 *
 * 【代码：首位化1】 for (int i = n; i >= c; i--) a[r][i] /= a[r][c];
 * 动作：防线系数全部除以3 (注意：程序按倒序从实数位除起，保证3最后变成1)：
 *  [  1 2.66 0.33|  8 ]
 *
 * 【代码：主元轰炸消0】 a[i][j] -= a[r][j] * a[i][c];
 * 动作：防线下方每一行，减去 (首行系数1 * 自己的行首元素)
 *  行1 -= 行0 * (-1) => [ 0  4.66 1.33| 10 ]
 *  行2 -= 行0 * ( 2) => [ 0 -6.33 0.33| -9 ]
 *
 * 第一轮后矩阵变成：(此时 r++)
 *  [  1 2.66 0.33 |  8 ] -> 这行被完美定格
 *  [  0  4.66 1.33| 10 ]
 *  [  0 -6.33 0.33| -9 ]
 *
 * -----------------------------------------
 * 循环 第 1 列 (c=1, r=1)：
 * 【代码：对调】剩下的两行中，第1列绝对值最大是 |-6.33|，行1与行2对调：
 *  [  1 2.66  0.33|  8 ] -> 已定格
 *  [  0 -6.33 0.33| -9 ] -> 新防线，行 1
 *  [  0  4.66 1.33| 10 ]
 * 
 * 【代码：首位化1】 a[r][i] /= a[r][c]
 * 动作：防线系数除以 -6.33：
 *  [  0   1 -0.05 | 1.42 ]
 *
 * 【代码：消0】 a[i][j] -= a[r][j] * a[i][c]
 * 动作：行2 -= (防线 * 4.66)：
 *  行2 => [ 0   0  1.58 | 3.38 ]
 *
 * -----------------------------------------
 * 循环 第 2 列 (c=2, r=2)：
 * 【代码：化1】 行2 直接除以 1.58 -> [ 0   0   1 | 2.14 ]
 *
 * 此时得到完美上三角矩阵：
 *  [  1 2.66  0.33|  8   ]
 *  [  0   1  -0.05| 1.42 ]
 *  [  0   0    1  | 2.14 ]
 *  也就是自然解得了 x2 = 2.14，底座建成！
 *
 * -----------------------------------------
 * 【战后计算：回代清算 Back Substitution】
 * 代码：for (int i = n - 1; i >= 0; i--) 
 *          for (int j = i + 1; j < n; j++) 
 *              a[i][n] -= a[i][j] * a[j][n];
 *
 * 动作 1：i=1, j=2 (求x1)
 * a[1][3] -= a[1][2]*a[2][3] (即1.42 - (-0.05)*2.14) => x1 = 1.53
 * 动作 2：i=0, (求x0) 扣除累加后面的常数
 * x0 = 8 - (2.66 * 1.53) - (0.33 * 2.14) => x0 = 3.22
 */

// 浮点数线性方程组的主体
int gauss(int n, vector<vector<double>>& a) {
    int c, r; // c: 列控制, r: 行控制
    for (c = 0, r = 0; c < n; c++) {
        // [1. 寻主元]
        int t = r;
        for (int i = r + 1; i < n; i++)
            if (fabs(a[i][c]) > fabs(a[t][c])) t = i;

        // [判断废列] 针对浮点数精度极小值 eps 判定为 0
        if (fabs(a[t][c]) < eps) continue; 

        // [2. 对调主元行] 
        for (int i = c; i <= n; i++) swap(a[t][i], a[r][i]);

        // [3. 头部系数化1] 必须倒序，保全原始左端数据作为被除基准
        for (int i = n; i >= c; i--) a[r][i] /= a[r][c];

        // [4. 下层系数消0]
        for (int i = r + 1; i < n; i++)   // 扫过底部每一行
            if (fabs(a[i][c]) > eps)      // 本身是 0 的行无需浪费算力计算
                for (int j = n; j >= c; j--) // 必须倒序，保全乘数基准
                    a[i][j] -= a[r][j] * a[i][c];

        r++; // 有效方程锁定落位，r指针对齐下一行
    }

    // [战后分型：处理冗余或矛盾方程]
    // 为什么会出现 r < n？ 
    // 当矩阵存在线性相关时，某一列及以下的系数被彻底消为 0，导致找不到有效主元，触发了 `continue`。
    // 此时列指针 c 前进，但有效方程行指针 r 停滞。最终矩阵底部（第 r 至 n-1 行）的左侧系数必然全为 0：
    // 形态 1 (无解):      [ 0  0  0 | 5 ] -> 0*x0 + 0*x1 + 0*x2 = 5，代数悖论。
    // 形态 2 (无穷多解):  [ 0  0  0 | 0 ] -> 0 = 0，恒等式。缺少约束独立方程，存在自由变量。
    if (r < n) {
        for (int i = r; i < n; i++)
            if (fabs(a[i][n]) > eps) 
                return 2; // 发现形态 1 (0 == 非零常数)，无解
        return 1; // 全是形态 2 (0 == 0)，无穷多组解
    }

    // 【Step 5: 回代填数】矩阵已成完美上三角，倒序反洗
    for (int i = n - 1; i >= 0; i--)       // 从底层向上走
        for (int j = i + 1; j < n; j++)    // 拿底层已求出的纯净答案 j
            // 把本行的总常数 a[i][n] ，减去 (毒瘤系数 a[i][j] * 纯净答案 a[j][n])
            a[i][n] -= a[i][j] * a[j][n];
            // 内层循环走完，常数格 a[i][n] 里存放的就是第 i 个未知数的绝对纯净答案

    return 0; // 【有唯一解】
}


