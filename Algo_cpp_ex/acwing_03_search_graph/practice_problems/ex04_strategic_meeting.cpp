#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

const long long INF = 1e18;
const int MAXN = 100010;

// Problem 4: 战略会议 (树的重心 / 换根DP)
//
// 题目描述:
// 公司架构是一棵树，有 N 个节点。边有权值（距离）。
// 求一个节点 u，使得所有其他节点到 u 的距离之和最小。
// 输出最小的总距离。

struct Edge {
    int to;
    int weight;
};

vector<Edge> adj[MAXN];
int sz[MAXN]; // 子树大小
long long dist_sum[MAXN]; // 换根DP状态：以 i 为根的总距离
int n;
long long ans = INF;

// 第一次 DFS: 计算子树大小
void dfs1(int u, int p) {
    sz[u] = 1;
    for (auto& edge : adj[u]) {
        int v = edge.to;
        if (v == p) continue;
        
        dfs1(v, u);
        sz[u] += sz[v];
    }
}

// 辅助 DFS: 计算 dist_sum[1] (所有点到根节点 1 的距离)
void get_root_dist(int u, int p, long long current_dist) {
    dist_sum[1] += current_dist;
    for (auto& edge : adj[u]) {
        int v = edge.to;
        if (v == p) continue;
        get_root_dist(v, u, current_dist + edge.weight);
    }
}

// 第二次 DFS: 换根 DP
// 状态转移方程:
// 当根从 u 移动到 v (u -> v, 边权 w):
// v 子树内的点 (sz[v] 个) 到新根 v 的距离减少 w
// v 子树外的点 (n - sz[v] 个) 到新根 v 的距离增加 w
// dp[v] = dp[u] - sz[v] * w + (n - sz[v]) * w
//       = dp[u] + (n - 2 * sz[v]) * w
void dfs2(int u, int p) {
    ans = min(ans, dist_sum[u]);
    
    for (auto& edge : adj[u]) {
        int v = edge.to;
        int w = edge.weight;
        if (v == p) continue;
        
        dist_sum[v] = dist_sum[u] + (long long)(n - 2 * sz[v]) * w;
        dfs2(v, u);
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    
    if (cin >> n) {
        for (int i = 0; i < n - 1; i++) {
            int u, v, w;
            cin >> u >> v >> w;
            adj[u].push_back({v, w});
            adj[v].push_back({u, w}); // 无向图
        }
        
        // 1. 初始化
        // 计算以节点 1 为根时的所有子树大小
        dfs1(1, 0); 
        
        // 计算 dist_sum[1] (所有点到 1 的距离和)
        dist_sum[1] = 0;
        get_root_dist(1, 0, 0);
        
        // 2. 换根 DP
        dfs2(1, 0);
        
        cout << ans << endl;
    } else {
        // 示例数据
        cout << "请输入 N (节点数) 和 N-1 条边 (u v w)..." << endl;
        // 构造一个简单的树
        // 1-2(1), 1-3(1), 2-4(1)
        // 重心显然在 1 或 2?
        // 1: 到2(1), 到3(1), 到4(2) -> 4
        // 2: 到1(1), 到3(2), 到4(1) -> 4
        // 3: 到1(1), 到2(2), 到4(3) -> 6
        // 4: 到1(2), 到2(1), 到3(3) -> 6
        cout << "示例输出: 4" << endl;
    }

    return 0;
}

/*
 * ==================================================================================
 * 【深度解析：树的重心 与 换根 DP 核心奥义】
 * ==================================================================================
 *
 * 1. 什么是 "换根 DP" (Re-rooting DP)？
 *    -------------------------------------------------------------------------------
 *    (A) 痛点剖析：
 *        题目给定了一棵具有 N 个节点的树（N 最大 100,000），要求选出一个“会议中心”节点，
 *        使得【所有人走到这个节点的总距离最短】。
 *        最暴力的做法：
 *        遍历所有的点，把每一个点都当作一次“根”，然后跑一次 DFS 算出所有点到它的距离之和。
 *        - 跑一次 DFS 的复杂度是 O(N)。
 *        - 遍历 N 个点做起点，总复杂度是 O(N^2)。
 *        当 N = 100,000 时，计算量达到 100 亿，绝对会超时 (TLE)。
 *
 *    (B) 破局之法：换根 DP 的基本思想
 *        想象一下，如果你已经千辛万苦算出了 "把会议定在节点 1 时，大家的总路程" (假设是 S)。
 *        现在，如果会议室从节点 1 **搬到了它的相邻节点 2**（假设边长为 W），总路程会发生什么变化？
 *        需要所有的点重新算一遍吗？**绝对不需要！**
 *
 *        我们可以像算账一样做简单的加减法：
 *        - 节点 2 以及它“背后”的那帮人（即节点 2 的子树节点）：他们原来去 1 开会，现在去 2 开会。
 *          显然，这帮人的路程都被**缩短**了 W！
 *          缩短总距离 = `(节点2子树的节点数) * W`。
 *        - 节点 1 的其他方向的人（即整个图中不在节点 2 子树里的人）：他们原来去 1 开会，现在去 2 开会。
 *          显然，这帮人必须先跑到 1，再多走一段 W 才能到 2，所以他们的路程都被**拉长**了 W！
 *          增加总距离 = `(全局总节点数 N - 节点2子树的节点数) * W`。
 *
 *        这就是极其优雅的**状态转移方程**：
 *        新总路程 = 旧总路程 - (向着会议室走来的人数)*W + (背着会议室被拉远的人数)*W
 *        公式化：dp[v] = dp[u] - sz[v]*w + (N - sz[v])*w = dp[u] + (N - 2*sz[v]) * w
 *
 *    (C) O(N) 的降维打击：
 *        有了这个方程，我们只需要：
 *        1. 先随便选一个点（比如点 1）做为绝对的老祖宗，跑一边 O(N) 的老实人 DFS，老老实实算出:
 *           - 每个节点的子树里有多少人 (`sz` 数组)
 *           - 所有人到点 1 的初始总距离 (`dist_sum[1]`)
 *        2. 然后再跑一边 O(N) 的轻功 DFS（从老祖宗开始像水波一样推演），
 *           利用上面的状态转移方程，瞬间求出所有点做会议室时的总距离！
 *        总复杂度瞬间从 O(N^2) 降维成 O(N)！！
 *
 * 2. 两次 DFS 到底干了什么？（全模拟推演）
 *    -------------------------------------------------------------------------------
 *    【预准备：数据结构】
 *    `sz[u]`：以 1 为绝对根节点的视角看，以 `u` 为根的“家族（子树）”里总共有多少个人（包含 u 自己）。
 *    `dist_sum[x]`：如果把会议室定在 x，全公司人走过来的总花费。
 *
 *    【Step 1: 先找一个“参照物” (dfs1 + get_root_dist)】
 *    我们必须先建立一个初始账本，我们假定节点 1 就是大老板，所有人先算算去 1 开会要花多少钱。
 *    - `dfs1(1, 0)`: 老板发话了：“查户口！”
 *      节点 1 把调查令发给下面，每个节点递归上报自己家族的人口。
 *      `sz[u] = 1` (自己算1人) + `sz[儿子A]` + `sz[儿子B]`...
 *      这个过程结束后，每个人都知道了自己的小圈子有几号人。
 *    - `get_root_dist(1, 0, 0)`: 老板又发话了：“每个人把到我办公室的打车费报上来！”
 *      递归地把每个人的路费累加，最后都存在了 `dist_sum[1]` 这个初始总账本里。
 *
 *    【Step 2: 换根推演 (dfs2)】
 *    由于算过初始总账了，现在会议室可以顺着连线，一家一家地挨个往下搬了，这就是 `dfs2`！
 *    假设会议室在 `u`，现在打算搬去相连的 `v` （搬家距离为 `w`）：
 *    
 *    - 当搬到 `v` 时，得利者是 `v` 的子树成员，一共 `sz[v]` 个人，他们每人少走 `w`。
 *      (亏损：-sz[v]*w)
 *    - 吃亏者是除了 `v` 家族以外的所有人，一共是 `n - sz[v]` 个人，他们每人都得多走 `w`。
 *      (盈利：+(n - sz[v])*w)
 *    
 *    账本秒算更迭：
 *    `dist_sum[v] = dist_sum[u] - sz[v]*w + (n - sz[v])*w`
 *    数学化简一下： `dist_sum[u] + (n - 2*sz[v]) * w`
 *    
 *    拿着这个公式，不断把会议地点顺着路分发给底下每一个员工家，算出的总账立刻进行 `ans = min(ans, ...)` 的比对，
 *    最后揪出来的最小的那个总账，就是天下的最优解。
 *
 * 3. 核心代码按步精讲
 *    -------------------------------------------------------------------------------
 *    【邻接表的选择 STL vector】
 *    对比练习2（因为 SPFA 卡常数和缓存命中率使用了链式前向星），对于这种仅仅跑两遍 DFS 的树形遍历问题，
 *    使用 `vector<Edge> adj[MAXN]` 代码更直观，足够应付，不需要纠结常数级别优化。
 *
 *    【防止 DFS 走回头的神级小技巧: `p` 参数】
 *    由于这棵树是当成无向图双向建边的（1 连 2，2 连 1），DFS 的时候最怕就是 1 跑到 2，2 转头又跑回 1，死循环。
 *    怎么破？带个干爹：`void dfs1(int u, int p)`
 *    `u` 是当前节点，`p` 是进来的父节点（即它干爹）。
 *    在遍历连线时 `if (v == p) continue;` —— 只要这个点是刚才派我下来的上级，我就坚决不回去！完美避开死循环。
 *
 *    【dfs2 中的局部物理与宏观视角切换】
 *    ```cpp
 *    dist_sum[v] = dist_sum[u] + (long long)(n - 2 * sz[v]) * w;
 *    ```
 *    这段代码最容易绕晕人的地方在于视角的割裂：
 *    - 你必须死死记住：`sz[v]` 这个数组的数值，是早在 **Step 1 以节点 1 为死根** 的图景里，就已经**写死在账本上**的数据！
 *    - 无论现在 `dfs2` 把会议室推演换到了树的哪个疙瘩角落，`sz[v]` 代表的永远是：
 *      **"当初以 1 为塔顶往下看时，挂在 v 下面（且被 v 包含）的那串葡萄有几个点。"**
 *    - 为什么这是对的？因为当你从 `u` 向 `v` 走过去的时候，`v` 的背后只有那一串葡萄！其他所有人都在你前进的后背方向。
 *    - 只有当树的相对方向被理清，才能用这一套状态转移进行无敌计算。
 *
 * 4. 树的重心的物理学视角
 *    -------------------------------------------------------------------------------
 *    这道题本质上就是在找“树的重心”。什么是重心？
 *    把这棵树想象成一个用铁丝连起来的棉花糖模型，如果有某个节点，你提着它把整棵树拎起来的时候，
 *    发现**挂在它下面最大的一坨棉花糖，它的重量都不超过总重量的一半（sz <= N/2）**，
 *    那么这个点就是这棵树的重心（平衡点）。
 *
 *    从上面我们推导的公式也可以看出：
 *    当往某个相邻点 v 移动时：`变化量 = (n - 2*sz[v]) * w`。
 *    - 如果 `sz[v] > n/2`，那么 `2*sz[v] > n`，括号里是个**负数**！
 *      意思是往 v 那个方向走，总距离会**变小**！那就应该往 v 走！
 *    - 如果往周围任何一个方向走，发现所有的 `sz[v]` 都 `< n/2`，那么无论怎么走变化量都是**正数（距离变大）**。
 *      说明当前待着不动的这个据点，就是风水宝地，最优解！
 * 
 * 5. 面试 / 竞赛防踩坑指南：
 *    -------------------------------------------------------------------------------
 *    1. `long long` 溢出爆炸：
 *       当节点数量 10^5，距离假设极大时，距离总和极有可能突破 `int` (最大约21亿)。
 *       这是最容易挂掉的坑，所以凡是跟 "dist_sum"、"总路程" 有关的，全家换用 `long long`。
 *       连带有乘法的中间公式也要小心：`(long long)(n - 2 * sz[v]) * w` 进行强转，避免溢出。
 *    2. 无向图开双倍边（本题 vector 免去烦恼，但如果是链式前向星 MAXM 必须开两倍N）。
 * ==================================================================================
 */
