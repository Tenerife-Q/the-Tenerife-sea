/*
这是一个非常深刻且直击算法灵魂的问题！把这四个问题搞懂，你就彻底掌握了“整除分块”和“数论基础”的精髓。

为了让你最容易理解，我们稍微调整一下回答顺序，先解释变量含义，再看分界点，最后算逆元。

---

问题 1：`n/k` 到底代表什么？（核心：角色的互换！）

在代码的两部分中，变量 `k` 的身份发生了**彻底的反转**，这就导致了 `n / k` 的含义完全不同。

*   **在第一部分（小约数）：** `k` 的身份是 **“约数本身”**（比如 k=2）。
    *   此时 `n / k` 代表：**这个约数在 1~n 中出现的“次数”**。
    *   *(比如 $n=20, k=2$，约数2出现了 $20/2=10$ 次)*。
*   **在第二部分（大约数）：** `k` 的身份变成了 **“出现的次数”**（比如 k=2次）。
    *   此时 `n / k` 代表：**出现刚好 `k` 次的“最大约数是谁”**。
    *   *(比如 $n=20, k=2$ 次，说明刚好出现2次的最大约数是 $20/2=10$)*。

一句话总结：第一部分的 `n/k` 求的是次数，第二部分的 `n/k` 求的是最大约数边界。

---

问题 2：为什么恰巧就是 √n（根号 N）这个分界点？

这是数学中的**对称性**决定的。
我们要求的是 ∑ 约数 × 出现次数。
已知：**约数 × 出现次数 ≤ n**（即 d × ⌊n/d⌋ ≤ n）。

既然两个数乘积小于等于 n，那么**这两个数中，必定至少有一个数 ≤ √n**！
以 n=100 为例，√100=10：
*   如果约数 d ≤ 10，我们直接遍历约数（第一部分）。
*   如果约数 d > 10，那么它的出现次数必定 < 10！

所以，√n 是一个完美的“跷跷板支点”：
*   左边：约数小，次数大。我们就遍历约数。
*   右边：约数大，但次数小。我们就遍历次数。
这样，无论是遍历约数还是遍历次数，循环量都不会超过 √n，这就是为什么能把时间复杂度卡在 O(√n) 的原因。

---

问题 3：为什么第一二部分的限制条件不一样？且不能混用？

*   **第一部分条件：`k * k <= n`**
    这里 k 是约数。等价于 k ≤ √n。意思很明确：“我要把所有 ≤ √n 的小约数全算完”。
*   **第二部分条件：`n / k > k`**
    这里 k 是次数。`n / k` 前面说了，代表“出现 k 次的最大约数”。
    这个条件的意思是：**我要找的这些约数 (n/k)，必须严格大于我在第一部分已经算过的 √n 界限 (k 这里本质上代表着界限，因为次数 ≤ √n)**。

为什么不能混用？为什么第二部分不写成 `k * k <= n`？
理论上，第二部分的次数 k 确实也是 ≤ √n 的，写成 `k * k <= n` 有时也不会报错。但是，写成 `n / k > k` 是为了**绝对防止重复计算**！

假设 n=20，√20 ≈ 4.47。
第一部分（约数 k）：算了 1,2,3,4。
第二部分（次数 k）：
*   当次数 k=4 时，对应的最大约数 R = 20/4 = 5。因为 5 > 4 (即 `n/k > k`)，成立，处理约数 5。
*   假如继续算当次数 k=5 时，对应的最大约数 R = 20/5 = 4。如果执行了，你就会把约数 4 又算一遍！
*   但是用 `n/k > k` 判断，当 k=5 时，20/5 = 4，4 > 5 是错的，循环直接终止！

所以，`n / k > k` 是一堵完美的防火墙，它保证了第二部分算出来的约数，**永远大于**次数，正好无缝衔接第一部分没算完的那些大约数，一个不多，一个不少！

---

问题 4：为什么 2 对 (10^9+7) 取模的逆元是 500000004？

我们在计算等差数列和 (L+R) × 项数 / 2 时，有个除以 2 的操作。
在模运算中（取模 M=10^9+7），**不能直接除以一个数，而是要乘以它的“乘法逆元”**。

“2 在模 M 下的逆元是 x”的数学定义是：
2 × x ≡ 1 (mod M)

我们希望 2 × x = M + 1，使得除以 M 余 1，代入 M = 1000000007：
2 × x = 1000000008 => x = 500000004。

验证：2 × 500000004 = 1000000008 ≡ 1 (mod 1000000007)。因此 500000004 是 2 的逆元。

所以在代码里，把 /2 替换为 *500000004 % MOD 即可。
*/

#include <iostream>

using namespace std;
using ll = long long;

// 常量定义
const ll M = 1000000007;     // 模数
const ll INV2 = 500000004;   // 2的模逆元，计算 (a/2 % M) 时使用 (a * INV2 % M)

/* =========================================================================
   🐢 SOLUTION 1: 线性遍历法 (暴力法)
   复杂度: O(N)
   状态: 已注释 (对于 10^12 会超时)
   -------------------------------------------------------------------------
int main_solution_1() {
    ll n;
    cin >> n;
    ll s = 0;
    for (ll k = 1; k <= n; k++) {
        // 直接计算每个约数 k 的贡献
        ll count = (n / k) % M;
        ll value = k % M;
        s = (s + (count * value) % M) % M;
    }
    cout << s << endl;
    return 0;
}
========================================================================= */

// 辅助函数：计算等差数列 [a, b] 的和：(a + b) * (b - a + 1) / 2
ll sum_range(ll a, ll b) {
    ll first_plus_last = (a + b) % M;       // (首项 + 末项)
    ll count = (b - a + 1) % M;             // 项数
    // 这里的 INV2 相当于 / 2
    return first_plus_last * count % M * INV2 % M;
}

/**
 * 🚀 SOLUTION 2: 根号分治 (整除分块优化)
 * 复杂度: O(sqrt(N))
 * 逻辑：将计算分为“小约数单独算”和“大约数按出现次数打包算”
 */
int main() {
    // 提升 I/O 效率
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll n;
    if (!(cin >> n)) return 0;

    ll total_sum = 0;

    // --- 第一部分：处理小约数 (k <= sqrt(n)) ---
    // 这里的 k 是约数本身。因为 sqrt(10^12) = 10^6，循环量很小。
    for (ll k = 1; k * k <= n; k++) {
        ll count = (n / k) % M;  // 约数 k 出现的次数
        ll value = k % M;        // 约数本身的值
        total_sum = (total_sum + (count * value) % M) % M;
    }

    // --- 第二部分：处理大约数 (打包计算) ---
    // 重点：这里的 k 代表“出现的次数”！
    // 我们寻找“哪些约数刚好出现了 k 次”，它们构成一个等差数列 [L, R]。
    for (ll k = 1; n / k > k; k++) {
        // R: 出现 k 次的最大约数是谁？
        ll R = n / k; 
        
        // L: 出现 k 次的最小约数是谁？
        // 逻辑：找出现 k+1 次的最大约数，然后 +1
        ll L = n / (k + 1) + 1; 

        // 举例：n=100, k=1时：
        // R = 100 / 1 = 100
        // L = 100 / (1+1) + 1 = 51
        // 说明 51~100 这些约数在 1~100 中都只出现了 1 次。

        // 计算这个区间内所有约数的总和
        ll block_sum = sum_range(L, R);
        
        // 这一坨约数每个都出现了 k 次
        ll contribution = (block_sum * (k % M)) % M;
        
        total_sum = (total_sum + contribution) % M;
    }

    cout << total_sum << "\n";

    return 0;
}



#include <iostream>
#include <vector>
using namespace std;
using ll = long long;

const ll MOD = 1000000007;
const ll MOD_EXP = 1000000006; // 指数层面的模数 (M-1)
const ll INV2 = 500000004;    // 2的模逆元

ll sum_range(ll a, ll b) {
    ll first_plus_last = (a + b) % MOD;
    ll count = (b - a + 1) % MOD;
    return first_plus_last * count % MOD * INV2 % MOD;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll n;
    cin >> n;

    ll total_sum = 0;

    for(ll k = 1; k * k <= n; k++) {
        ll count = (n / k) % MOD;
        ll value = k % MOD;
        total_sum = (total_sum + (count * value) % MOD) % MOD;
    }

    for (ll k = 1; n / k > k; k++) {
        ll R = n / k;
        ll L = n / (k + 1) + 1;
        ll block_sum = sum_range(L, R);
        ll contribution = (block_sum * (k % MOD)) % MOD;
        total_sum = (total_sum + contribution) % MOD;
    }
    cout << total_sum << "\n";

    return 0;
}