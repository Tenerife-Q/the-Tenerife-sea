/*
 * ============================================================
 * 🚀 进阶技巧（完成上面 6 个后可以尝试）
 * ============================================================
 * 
 * 1️⃣ 条件断点
 *    按 F9 设断点后，右键点击断点 → "编辑断点"
 *    输入条件：i == 3  （只在 i 等于 3 时停止）
 *    这样可以快速定位特定数据的问题
 * 
 * 2️⃣ 日志点（Logpoint）
 *    右键设断点时，选择"添加日志点"
 *    输入：i = {i}, sum = {sum}
 *    程序不会暂停，但会在控制台打印变量值
 * 
 * 3️⃣ 表达式监视（Watch）
 *    调试时在 DEBUG 面板的 WATCH 输入框输入表达式
 *    例如：sum + i （实时计算表达式的值）
 * 
 * 4️⃣ 调用栈（Call Stack）
 *    DEBUG 面板显示函数调用的返回地址
 *    点击可以回到调用该函数的位置
 */


/*
 * ============================================================
 * ❓ FAQ - 我卡住了怎么办？
 * ============================================================
 * 
 * Q: 按 Ctrl+Shift+B 说找不到编译器？
 * A: 检查 MinGW 是否安装在 D:\mingw64\bin\g++.exe
 *    如果路径不同，修改 .vscode/tasks.json 中的 command 字段
 * 
 * Q: 按 F5 后没反应或弹出错误？
 * A: 检查 .vscode/launch.json 的 preLaunchTask 字段
 *    确保值是 "build"（与 tasks.json 中的 label 匹配）
 * 
 * Q: 设了断点但程序没停下来？
 * A: 检查：
 *    1. 断点行是否是可执行代码（不是注释或空行）
 *    2. 是否按了 F5 启动调试（不能只是 Ctrl+F5）
 *    3. 程序是否真的执行到了那一行
 * 
 * Q: 按 F11 时光标没有进入函数，反而跳过了？
 * A: 这可能是系统库函数（如 cout）
 *    对于自定义函数，F11 应该能进入
 *    如果是库函数，用 F10 跳过，继续调试自己的代码
 * 
 * Q: DEBUG 面板空空如也，看不到变量值？
 * A: 检查是否：
 *    1. 真的在断点处暂停了（看底部"DEBUG CONSOLE"）
 *    2. 变量是否在当前作用域内
 *    3. 编译时是否加了 -g 选项（debug 信息）
 */


