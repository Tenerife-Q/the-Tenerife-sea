/*
 * ============================================================
 * 🎮 实战流程（以每个文件为例）
 * ============================================================
 * 
 * 第 1 步：打开目标文件
 * ├─ 在 VS Code 中打开 debug_scenario_2_logic.cpp
 * 
 * 第 2 步：阅读注释
 * ├─ 理解"学习目标"是什么
 * ├─ 知道这个文件有什么问题
 * 
 * 第 3 步：编译或运行
 * ├─ 按 Ctrl+Shift+B 编译（或 Ctrl+F5 运行）
 * ├─ 看能否成功，如果失败则修复编译错误
 * 
 * 第 4 步：发现问题
 * ├─ 如果能编译/运行，试试能否看到输出
 * ├─ 如果输出不对，说明有逻辑错误
 * ├─ 不要急着修复！进入调试模式
 * 
 * 第 5 步：设置调试点
 * ├─ 按照代码注释 📌 的位置，点击左侧行号区域设断点（F9）
 * ├─ 断点处会出现红色圆点
 * 
 * 第 6 步：启动调试
 * ├─ 按 F5，程序运行到第一个断点暂停
 * ├─ 右侧 DEBUG 面板会显示所有局部变量及其值
 * 
 * 第 7 步：单步执行
 * ├─ 按 F10 执行当前行，然后暂停在下一行
 * ├─ 观察变量值的变化
 * ├─ 重复按 F10，直到发现值不符合预期
 * 
 * 第 8 步：分析错误
 * ├─ 对比预期值和实际值
 * ├─ 根据代码注释的提示推断出 bug 原因
 * ├─ 参考"修复方案"部分验证你的推论
 * 
 * 第 9 步：修复代码
 * ├─ 按 Shift+F5 停止调试
 * ├─ 修改代码（按照代码注释中的"修复方案"）
 * 
 * 第 10 步：验证修复
 * ├─ 再次按 Ctrl+Shift+B 和 Ctrl+F5
 * ├─ 确认输出结果现在是正确的
 * ├─ 如果还有问题，重复第 5-9 步
 */


