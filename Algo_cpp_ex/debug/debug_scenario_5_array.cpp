#include <iostream>
using namespace std;

int main(){
    int arr[5] = {10, 20, 30, 40, 50};  // 数组有 5 个元素，下标 0-4
    
    // ❌ 问题：循环条件错了
    for(int i = 0; i <= 5; i++){  // 应该是 i < 5 还是 i <= 4？
        cout << "arr[" << i << "] = " << arr[i] << " ";
        // 📌 在这行按 F9 设置断点
        // 然后按 F5 启动调试，观察 i 和 arr[i]
    }
    
    cout << endl;
    
    return 0;
}

