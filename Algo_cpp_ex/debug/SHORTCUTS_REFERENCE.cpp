/*
 * F5 = 五根手指，按下开始执行 (5 = START)
 * 
 * F9 = 断点的"9"像圆点 (9 = 断点圆点)
 * 
 * F10 = 单步跳过 (10 = 跳过一格)
 * 
 * F11 = 单步进入，数字 11 拉长了 (11 = 拉长进去)
 * 
 * Shift+F11 = 向上箭头，退出函数 (↑ = 退出)
 * 
 * Ctrl+Shift+B = Build (B)
 * Ctrl+F5 = Run without debug (F without 5)
 */


