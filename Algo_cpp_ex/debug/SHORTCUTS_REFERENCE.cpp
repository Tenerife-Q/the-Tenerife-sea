// Ctrl+Shift+B
// 功能：编译当前打开的文件
// 用途：
//   1. 检查代码是否有编译错误
//   2. 生成 .exe 可执行文件
// 工作流：写代码 → Ctrl+Shift+B → 看是否有错误
// 示例：
//   在 hello.cpp 中，按 Ctrl+Shift+B
//   → 看底部输出 "生成已成功完成" 或显示编译错误
// 快捷键替代：菜单 → 终端 → 运行任务 → build


// Ctrl+F5
// 功能：运行程序（不启动调试器）
// 用途：
//   1. 快速查看程序输出
//   2. 不需要逐行检查时使用
// 工作流：写代码 → Ctrl+F5 → 看控制台输出 → 确认逻辑对不对
// 示例：
//   计算 1+2+...+10 的和，按 Ctrl+F5
//   → 控制台显示 "Sum = 55"
//   → 验证程序逻辑是否正确
// 特点：速度快，但无法查看变量细节


// F5
// 功能：启动调试器（带断点和单步执行）
// 用途：
//   1. 发现代码有逻辑问题时使用
//   2. 需要追踪变量值的变化
//   3. 查找 bug 的根本原因
// 工作流：
//   1. 在可疑的代码行按 F9 设置断点
//   2. 按 F5 启动调试
//   3. 程序暂停在断点处
//   4. 用 F10/F11 单步执行
//   5. 观察 DEBUG 面板中的变量值
// 示例：
//   发现 sum 值错误
//   → 在循环内按 F9 设断点
//   → 按 F5 启动调试
//   → 按 F10 逐行执行
//   → 看 sum 值如何变化
// 使用前提：需要先编译成功（文件有 -g 选项）


/*
 * ============================================================
 * 📋 第二部分：断点控制
 * ============================================================
 */

// F9
// 功能：设置/移除断点
// 用途：
//   1. 在想要暂停的代码行设置红色断点
//   2. 再按一次 F9 移除断点
// 工作流：
//   1. 点击代码行（光标在那一行）
//   2. 按 F9 设置断点
//   3. 左侧行号区域出现红色圆点
//   4. 按 F5 启动调试，程序会在此断点停止
//   5. 再在这一行按 F9，断点消失
// 示例：
//   for(int i = 1; i <= 10; i++){
//       sum = sum + i;
//   }
//   → 点击 "sum = sum + i" 这一行
//   → 按 F9（左侧出现红点）
//   → 按 F5，程序在此停止
// 技巧：可以设置多个断点，按 F5 后依次跳过


// Shift+F5
// 功能：停止调试
// 用途：
//   1. 停止正在运行的调试会话
//   2. 返回编辑状态
// 工作流：
//   1. 按 F5 启动调试后
//   2. 调试完毕，按 Shift+F5 停止
//   3. DEBUG 面板消失，回到编辑模式
// 示例：
//   正在单步执行 → 发现了 bug → 按 Shift+F5 停止
//   → 修改代码 → 再按 F5 重新调试


/*
 * ============================================================
 * 📋 第三部分：单步执行（调试的核心！）
 * ============================================================
 * 这些快捷键只在 F5 启动调试后才有效
 */

// F10
// 功能：单步跳过（Step Over）
// 作用：
//   执行当前行，然后停在下一行
//   如果当前行有函数调用，不会进入函数内部，直接跳过
// 用途：
//   1. 逐行执行代码
//   2. 观察每一行的执行结果
//   3. 跳过已知没问题的函数
// 工作流：
//   1. 按 F5 启动调试（程序停在第一个断点）
//   2. 重复按 F10 逐行执行
//   3. 每按一次，程序执行一行，然后暂停
//   4. 观察 DEBUG 面板中变量变化
// 示例（单步跳过）：
//   ```cpp
//   int x = 5;              ← 光标在这，按 F10
//   y = calculate(x);       ← F10 执行上一行，现在停在这
//   cout << y << endl;      ← 再按 F10，跳过 calculate 函数，停在这
//   ```
//   结果：不会进入 calculate 函数内部
// 好处：快速执行，不陷入函数细节


// F11
// 功能：单步进入（Step Into）
// 作用：
//   执行当前行，如果有函数调用会进入函数内部
//   可以查看函数内部的执行过程
// 用途：
//   1. 深入函数内部调查
//   2. 追踪函数的参数和局部变量
//   3. 找到函数内部的 bug
// 工作流：
//   1. 按 F5 启动调试
//   2. 光标在函数调用的那一行
//   3. 按 F11 进入函数
//   4. 光标跳进函数内部代码
//   5. 继续按 F10 或 F11 逐行执行
// 示例（单步进入）：
//   ```cpp
//   int x = 5;
//   y = calculate(x);    ← 光标在这，按 F11
//   // ↓ 光标跳进函数内部 ↓
//   int calculate(int a){
//       int result = a * 2;   ← 现在停在这，可以看函数内部
//       return result;
//   }
//   ```
//   结果：进入了 calculate 函数，可以逐行跟踪
// 注意：如果是系统库函数（cout 等），F11 可能进不去


// Shift+F11
// 功能：单步跳出（Step Out）
// 作用：
//   从当前函数快速跳出，停在调用该函数的下一行
//   即使函数还有代码没执行，也会直接跳出
// 用途：
//   1. 已经找到函数内的问题，想快速退出
//   2. 进入函数后发现不需要看内部细节
//   3. 想回到调用函数的地方
// 工作流：
//   1. 用 F11 进入某个函数
//   2. 发现不需要看函数细节
//   3. 按 Shift+F11 快速跳出
//   4. 光标回到调用该函数的地方
// 示例（单步跳出）：
//   ```cpp
//   y = calculate(x);  ← 曾经按 F11 进入这个函数
//   // 现在在函数内部
//   int calculate(int a){
//       int result = a * 2;
//       int debug = result * 3;
//       // ... 更多代码 ...
//       // 按 Shift+F11，直接跳出
//       // ↓ 光标回到下一行 ↓
//   }
//   cout << y << endl;  ← Shift+F11 后，停在这
//   ```
//   结果：函数内剩余代码不执行，直接跳出


/*
 * ============================================================
 * 📋 第四部分：查看变量值（调试的必杀技！）
 * ============================================================
 */

// 在 DEBUG 面板中看变量
// 位置：VS Code 左侧 → 调试图标 → VARIABLES 栏
// 内容：
//   Local：当前函数的局部变量和参数
//   Static：静态/全局变量
// 使用方法：
//   1. 按 F5 启动调试
//   2. 程序停在断点时，自动显示 VARIABLES
//   3. 看变量名、类型和值
// 示例：
//   ```cpp
//   for(int i = 1; i <= 10; i++){
//       sum = sum + i;  ← 在这里设断点 F9
//   }
//   ```
//   按 F5 启动，在 VARIABLES 看：
//   - i = 1
//   - sum = 0
//   按 F10 后，看：
//   - i = 2
//   - sum = 1
//   继续按 F10，逐次观察
// 技巧：
//   - 展开复杂变量（数组、结构体）以查看详细值
//   - 可以在 WATCH 栏手动输入表达式，实时计算


// Ctrl+Shift+M
// 功能：打开 Problems 面板（问题面板）
// 用途：
//   1. 查看编译时的错误和警告
//   2. 快速跳转到有错误的代码行
// 工作流：
//   1. 编译代码时发现错误
//   2. 按 Ctrl+Shift+M 打开 Problems
//   3. 看错误列表，点击某个错误
//   4. 自动跳转到出错的代码行
// 示例：
//   编译失败 → Ctrl+Shift+M → 看"error: expected ';'"
//   → 点击该错误 → 光标跳到缺少分号的地方


/*
 * ============================================================
 * 📋 第五部分：完整调试流程（实战）
 * ============================================================
 */

/*
 * 流程 1：快速测试（发现问题）
 * 
 * 1. 写完代码
 * 2. Ctrl+Shift+B       编译
 * 3. Ctrl+F5           运行看输出
 * 4. 发现输出不对 → 进入流程 2
 */


/*
 * 流程 2：追踪变量（找 bug）
 * 
 * 1. 按 F9               在可疑代码行设置断点
 * 2. 按 F5               启动调试
 * 3. 按 F10 多次         逐行执行
 * 4. 看 VARIABLES 面板    观察变量值变化
 * 5. 发现异常数值 → 推断 bug 原因
 * 6. Shift+F5            停止调试
 * 7. 修改代码
 * 8. Ctrl+Shift+B        重新编译
 * 9. F5 再调试或 Ctrl+F5  直接运行验证
 */


/*
 * 流程 3：深入函数（追踪函数内部）
 * 
 * 1. 在调用函数的那一行设置断点（F9）
 * 2. F5 启动调试
 * 3. 按 F11 进入函数内部
 * 4. 按 F10 逐行执行函数内的代码
 * 5. 观察参数值和局部变量值
 * 6. 或按 Shift+F11 快速跳出
 * 7. Shift+F5 停止调试
 */


