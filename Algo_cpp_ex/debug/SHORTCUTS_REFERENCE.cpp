/*
 * ============================================================
 * 📋 第七部分：常见场景对应快捷键
 * ============================================================
 */

/*
 * ❓ 场景：代码有语法错误，我要找到它在哪
 * 
 * ✅ 方案：
 * 1. Ctrl+Shift+B  (编译)
 * 2. Ctrl+Shift+M  (打开 Problems 看错误列表)
 * 3. 点击错误项，自动跳转到出错位置
 */


/*
 * ❓ 场景：程序能编译运行，但输出结果不对
 * 
 * ✅ 方案：
 * 1. Ctrl+F5  (运行看输出，确认有问题)
 * 2. F9       (在关键代码行设置断点)
 * 3. F5       (启动调试)
 * 4. F10 连按  (逐行执行，观察变量值)
 * 5. 发现异常变量值 → 推断 bug
 */


/*
 * ❓ 场景：这个函数是怎么工作的？需要看函数内部
 * 
 * ✅ 方案：
 * 1. F9       (在函数调用那一行设置断点)
 * 2. F5       (启动调试)
 * 3. F11      (进入函数内部)
 * 4. F10 连按  (逐行执行函数内代码)
 * 5. Shift+F11(如果不需要继续看，快速跳出)
 * 6. Shift+F5 (停止调试)
 */


/*
 * ❓ 场景：程序陷入死循环，无法停止
 * 
 * ✅ 方案：
 * 1. Ctrl+C   (在终端按 Ctrl+C 停止程序)
 * 2. 再试一遍，这次在循环行设置断点 F9
 * 3. F5       (启动调试，会停在断点)
 * 4. F10      (观察循环变量是否在改变)
 * 5. 找到循环条件错误
 */


