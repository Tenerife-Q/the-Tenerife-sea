#include <iostream>
#include <string> // 必须包含
#include <algorithm> // 包含 reverse
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    // ---------------------------------------------------------
    // ⌨️ 1. 字符串读取
    // ---------------------------------------------------------
    string s1, s2;
    // 假设输入：Hello World
    // cin >> s1; 
    // cout << s1; // 只会读到 "Hello"，因为 cin 遇到空格就停止！
    
    // 📝 技巧：读取整行 (包含空格)
    // string line;
    // getline(cin, line); 
    // cout << line;
    
    // ---------------------------------------------------------
    // 🔗 2. 拼接与基本操作
    // ---------------------------------------------------------
    s1 = "Hello";
    s2 = "World";
    
    string s3 = s1 + " " + s2 + "!"; // 直接相加，超级方便
    cout << s3 << "\n"; // 输出: Hello World!
    
    cout << "Length: " << s3.length() << " (or " << s3.size() << ")\n";
    
    // ---------------------------------------------------------
    // 🔍 3. 查找与子串
    // ---------------------------------------------------------
    // find() 返回下标，找不到返回 string::npos
    size_t pos = s3.find("World");
    if (pos != string::npos) {
        cout << "'World' found at index: " << pos << "\n";
    }
    
    // substr(start_index, length)
    string sub = s3.substr(0, 5); // 从 0 开始，取 5 个字符
    cout << "Substring: " << sub << "\n"; // Hello

    // ---------------------------------------------------------
    // ⚖️ 4. 字典序比较
    // ---------------------------------------------------------
    // 就像查字典一样比大小
    string a = "apple";
    string b = "banana";
    
    if (a < b) {
        cout << a << " comes before " << b << "\n";
    }
    
    // ---------------------------------------------------------
    // 🔄 5. 反转字符串
    // ---------------------------------------------------------
    reverse(a.begin(), a.end());
    cout << "Reversed apple: " << a << "\n";

    return 0;
}
