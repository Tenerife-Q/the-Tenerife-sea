#include <iostream>
#include <vector> // 必须包含这个头文件
using namespace std;

// 🟢 技巧：大数组要定义在 main 函数外面（全局变量）
// 因为 main 里面的内存（栈）很小，大约只有 few MB
// 全局区域（堆/静态区）很大，可以开很大的数组（如 10^7）
const int MAX_N = 100005;
int global_array[MAX_N]; 

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    // ---------------------------------------------------------
    // 📦 1. 静态数组 (C Style)
    // ---------------------------------------------------------
    int small_arr[10] = {1, 2, 3}; // 未初始化的部分默认为 0 (在全局) 或 随机值 (在局部)
    // 建议：局部数组一定要初始化！
    
    // ---------------------------------------------------------
    // 🚀 2. std::vector (推荐)
    // ---------------------------------------------------------
    // vector 是变长数组，不仅安全，而且提供很多方便的方法
    
    vector<int> v; // 定义一个空的 vector
    
    // 插入元素 (push_back) - 自动扩容
    v.push_back(10);
    v.push_back(20);
    v.push_back(30);
    
    cout << "Vector contents: ";
    for(int i = 0; i < v.size(); i++) {
        cout << v[i] << " "; // 像数组一样访问
    }
    cout << "\n";
    cout << "Current size: " << v.size() << "\n";
    
    // ---------------------------------------------------------
    // 💡 3. vector 常用初始化方式
    // ---------------------------------------------------------
    // 定义一个大小为 5，所有值都为 -1 的数组 (适合做 DP 初始化)
    // DP是动态规划(Dynamic Programming)的简称，常用于解决最优化问题
    vector<int> dp(5, -1); 
    
    cout << "DP init: ";
    // C++11 增强 for 循环 (foreach) - 竞赛也很常用
    for(int x : dp) {
        cout << x << " ";
    }
    cout << "\n";

    // ---------------------------------------------------------
    // 🧊 4. 二维数组 (Matrix)
    // ---------------------------------------------------------
    // 用 vector 定义 n 行 m 列的二维数组
    int n = 3, m = 4;
    vector<vector<int>> matrix(n, vector<int>(m, 0)); // 初始化全 0
    
    matrix[1][1] = 99; // 修改第 2 行第 2 列
    
    cout << "Matrix [1][1]: " << matrix[1][1] << "\n";

    return 0;
}
