    // ---------------------------------------------------------
    // 🛑 3. Break 与 Continue 演示
    // ---------------------------------------------------------
    cout << "\n--- Loop Demo ---\n";
    for (int i = 1; i <= 5; i++) {
        if (i == 3) {
            cout << "Skip 3 (continue)\n";
            continue; // 跳过本次循环剩余部分，直接进入 i=4
        }
        if (i == 5) {
            cout << "Stop at 5 (break)\n";
            break; // 彻底退出循环
        }
        cout << "Processing: " << i << "\n";
    }

    return 0;
