#include <iostream>
#include <iomanip>
using namespace std;

int main() {
    double x;
    cout << "请输入一个数，程序将计算它的三次方根: ";
    if(cin >> x) {
        // [董晓板子 - 浮点数版本]
        // 浮点数也是一样的思想：
        // 蓝色区域：mid^3 < x
        // 红色区域：mid^3 >= x
        // 我们要找的就是红色区域的左边界 (第一个此时满足 >= x 的数，实质上就是立方根)
        
        // 1. 初始化边界
        // 立方根的范围：如果 x 是 0.001，根是 0.1，所以范围不能随便设为 0~x
        // 比较稳妥的是设一个绝对大的范围，比如 -10000 到 10000
        double l = -10000, r = 10000;
        
        // 2. 循环条件 
        // 整数二分我们用 l+1 != r
        // 浮点数二分我们用 r - l > 1e-8 (这其实就是判断 l 和 r 是否已经“撞”到一起了)
        // 还有一种更无脑的：直接循环 100 次
        for (int i = 0; i < 100; i++) { // 循环 100 次精度绝对够了
        // while (r - l > 1e-8) { // 或者用精度控制
            double mid = (l + r) / 2;
            
            // 3. check 函数 (判断染什么颜色)
            if (mid * mid * mid < x) {
                l = mid; // 是蓝色，L 追过去 (浮点数不需要 -1/+1，直接赋值)
            } else {
                r = mid; // 是红色，R 追过去
            }
        }

        // 最终 l 和 r 几乎重合，输出谁都行
        cout << fixed << setprecision(6) << l << endl;
    }

    return 0;
}
